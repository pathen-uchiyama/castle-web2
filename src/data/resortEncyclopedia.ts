import type { Resort } from "./types";

/* ═══════════════════════════════════════════════════════════════════
   TYPES
   ═══════════════════════════════════════════════════════════════════ */

export type ServiceType = "signature" | "table-service" | "quick-service" | "snack" | "lounge" | "dinner-show" | "character-dining" | "food-truck" | "kiosk" | "buffet" | "prix-fixe";
export type MealPeriod = "breakfast" | "lunch" | "dinner" | "brunch" | "snack" | "all-day";
export type PriceRange = "$" | "$$" | "$$$" | "$$$$";
export type HotelCategory = "value" | "moderate" | "deluxe" | "deluxe-villa" | "campground";
export type TransportMode = "bus" | "monorail" | "skyliner" | "boat" | "walking" | "minnie-van" | "car";
export type CrowdLevel = "low" | "moderate" | "high" | "very-high" | "extreme";
export type LandArea = { name: string; description: string; iconicAttraction?: string };
export type ThrillLevel = "gentle" | "moderate" | "thrilling" | "intense";
export type AttractionType = "ride" | "show" | "meet-and-greet" | "experience" | "parade" | "fireworks" | "play-area" | "water-ride";

/* ── Resort (top-level: WDW or DLR) ──────────────────────────────── */
export interface ResortDestination {
  resortId: Resort;
  name: string;
  fullName: string;
  location: string;
  size: string;
  opened: string;
  description: string;
  tagline: string;
  knownFor: string[];
  weatherSummary: string;
  weatherByMonth: { month: string; avgHigh: number; avgLow: number; rainDays: number; note: string }[];
  transportOverview: string;
  transportModes: TransportOption[];
  heroImage: string;
}

/* ── Hotels ──────────────────────────────────────────────────────── */
export interface ResortHotel {
  hotelId: string;
  name: string;
  resortId: Resort;
  category: HotelCategory;
  theme: string;
  description: string;
  location: string;
  nearestParks: string[];
  transportTo: { parkName: string; mode: TransportMode; estimatedMinutes: number }[];
  priceRange: { low: number; high: number; peakHigh: number; note: string };
  roomTypes: { name: string; sleeps: number; priceFrom: number }[];
  amenities: string[];
  pools: { name: string; features: string[] }[];
  diningOnSite: string[];
  rating: number;
  reviewCount: number;
  vibe: string;
  bestFor: string[];
  tips: string[];
  tags: string[];
}

/* ── Dining / Restaurants ────────────────────────────────────────── */
export interface ResortRestaurant {
  restaurantId: string;
  name: string;
  resortId: Resort;
  locationName: string;
  locationArea: string;
  cuisine: string;
  serviceType: ServiceType;
  mealPeriods: MealPeriod[];
  priceRange: PriceRange;
  priceNote: string;
  rating: number;
  reviewCount: number;
  description: string;
  vibe: string;
  signatureItems: string[];
  menuLink?: string;
  characterDining: boolean;
  requiresReservation: boolean;
  reservationDifficulty: "easy" | "moderate" | "hard" | "legendary";
  kidFriendly: boolean;
  dietaryAccommodations: string[];
  dressCode?: string;
  noShowFee?: string;
  cancelPolicy?: string;
  bestFor: string[];
  insiderTip: string;
  tags: string[];
}

/* ── Parks ────────────────────────────────────────────────────────── */
export interface ParkOverview {
  parkId: string;
  name: string;
  resortId: Resort;
  type: "theme-park" | "water-park";
  opened: string;
  size: string;
  description: string;
  tagline: string;
  icon: string;
  heroImage: string;
  operatingHours: { regular: string; earlyEntry: string; extendedEvening?: string };
  typicalCrowds: { day: string; level: CrowdLevel; note: string }[];
  bestDays: string[];
  worstDays: string[];
  lands: LandArea[];
  attractionCount: number;
  diningCount: number;
  mustDo: string[];
  tips: string[];
  knownFor: string[];
  tags: string[];
}

/* ── Attractions ────────────────────────────────────────────────── */
export interface ParkAttraction {
  attractionId: string;
  parkId: string;
  name: string;
  land: string;
  type: AttractionType;
  thrillLevel: ThrillLevel;
  heightRequirement?: string;
  duration: string;
  description: string;
  avgWaitMinutes: { low: number; typical: number; peak: number };
  lightningLane: boolean;
  lightningLaneType?: "individual" | "multi-pass";
  riderSwitch: boolean;
  singleRider: boolean;
  virtualQueue: boolean;
  mustDo: boolean;
  tip: string;
  tags: string[];
}

/* ── Transportation ──────────────────────────────────────────────── */
export interface TransportOption {
  mode: TransportMode;
  name: string;
  description: string;
  servesAreas: string[];
  cost: string;
  tip: string;
}

/* ── Shopping Areas ──────────────────────────────────────────────── */
export interface ShoppingArea {
  areaId: string;
  name: string;
  resortId: Resort;
  description: string;
  location: string;
  notableStores: { name: string; description: string; category: string }[];
  dining: string[];
  entertainment: string[];
  hours: string;
  parking: string;
  tags: string[];
}


